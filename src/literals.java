public class literals {
    public static void main(String[] args){
        // a const value which can be assigned to the variable
        // is called as a literal
        byte age =34;
        char ch ='a';
        float f1=3.4f;
        double d1 =83.33D;
        System.out.println(age);
        String a = "nikhil";
        System.out.println(a);
    }
}
